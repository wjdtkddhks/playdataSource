package Chapter4;

public class EqualityExmple1 {

	public static void main(String[] args) {
	
		
			int num1 = 5;
			int num2 = 2+3;
			
			if(num1 == num2)
				System.out.println("정답입니다");
			
			if((num1 != num2) != true)
				System.out.println("거짓입니다");
			else
				System.out.println("장난이지롱");
	}

}
