package Chapter4;

public class ShiftExmple1 {

	public static void main(String[] args) {
		int a = 10;
		int b = -10;
		
		
		System.out.println("a << 2 : " + (a<<2));
		System.out.println("b << 2 : " + (b<<2));
		System.out.println("a>> 2 : " + (a>>2));
		System.out.println("b>>2 : " + (b>>2));
		System.out.println("a>>>2 : " + (a>>>2));
		System.out.println("b>>>2 : " + (b>>>2));
		
	}

}
